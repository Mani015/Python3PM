
# How many ways to create a variable name
# Type1 : Single Value to assign a single variable name
v1 = 23
# print(v1)  23

# Type2 : Multiple values to assign a multiple variable names
x1,x2,x3 = 10,20,30
print("x1 is:",x1)
print('x2 is :',x2)
print('x3 is:',x3)
# x1 is: 10
# x2 is : 20
# x3 is: 30

# Type3 :  single value to assign a multiple variable names
x1 = x2 = x3 = x4 = 100


# Type4 : Multiple values to assign a multiple variable names in a single line by using semi colon ';'
s1 = 101; s2 = 102; s3 = 103; s4 = 104;s5 = 105

